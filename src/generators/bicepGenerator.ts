import { sanitizeName, toSnakeCase } from '../utils/codeFormatter';

/**
 * Utility to format a JS object as indented Bicep properties.
 */
function formatBicepProperties(
  obj: Record<string, unknown>,
  indent: number
): string {
  const pad = ' '.repeat(indent);
  const lines: string[] = [];

  for (const [key, value] of Object.entries(obj)) {
    if (value === null || value === undefined) continue;

    if (Array.isArray(value)) {
      lines.push(`${pad}${key}: [`);
      for (const item of value) {
        if (typeof item === 'object' && item !== null) {
          lines.push(`${pad}  {`);
          lines.push(
            formatBicepProperties(item as Record<string, unknown>, indent + 4)
          );
          lines.push(`${pad}  }`);
        } else {
          lines.push(`${pad}  ${formatBicepValue(item)}`);
        }
      }
      lines.push(`${pad}]`);
    } else if (typeof value === 'object') {
      lines.push(`${pad}${key}: {`);
      lines.push(
        formatBicepProperties(value as Record<string, unknown>, indent + 2)
      );
      lines.push(`${pad}}`);
    } else {
      lines.push(`${pad}${key}: ${formatBicepValue(value)}`);
    }
  }

  return lines.join('\n');
}

function formatBicepValue(value: unknown): string {
  if (typeof value === 'string') {
    if (value.startsWith('[') && value.endsWith(']')) {
      // Bicep expression
      return value.slice(1, -1);
    }
    return `'${value}'`;
  }
  if (typeof value === 'boolean') return value.toString();
  if (typeof value === 'number') return value.toString();
  return `'${String(value)}'`;
}

